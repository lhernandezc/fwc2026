export function getTeam(name) {
  for (const g of Object.values(GROUPS))
    for (const t of g.t) if (t.c === name) return t;
  return { c: name, f: "🏳️" };
}

export function calcStandings(gk, scores) {
  const st = {};
  GROUPS[gk].t.forEach(t => { st[t.c] = { ...t, pts:0, pj:0, pg:0, pe:0, pp:0, gf:0, gc:0, dg:0, form:[] }; });
  (MATCHES[gk] || []).forEach((m, i) => {
    const sc = scores[gk + "_" + i];
    if (sc && sc.h !== "" && sc.a !== "") {
      const h = parseInt(sc.h), a = parseInt(sc.a);
      const hs = st[m[0]], as = st[m[1]];
      if (!hs || !as) return;
      hs.pj++; as.pj++;
      hs.gf += h; hs.gc += a; hs.dg += h - a;
      as.gf += a; as.gc += h; as.dg += a - h;
      if (h > a) { hs.pts += 3; hs.pg++; as.pp++; hs.form.push("w"); as.form.push("l"); }
      else if (h === a) { hs.pts++; hs.pe++; as.pts++; as.pe++; hs.form.push("d"); as.form.push("d"); }
      else { as.pts += 3; as.pg++; hs.pp++; hs.form.push("l"); as.form.push("w"); }
    }
  });
  const teams = Object.values(st);
  const anyPlayed = teams.some(t => t.pj > 0);
  // If no matches played yet, preserve original group order (no alphabetical sort)
  if (!anyPlayed) return teams;
  return teams.sort((a, b) => b.pts - a.pts || b.dg - a.dg || b.gf - a.gf || b.pj - a.pj);
}

export function allStandings(scores) {
  const r = {};
  Object.keys(GROUPS).forEach(k => { r[k] = calcStandings(k, scores); });
  return r;
}

export function bestThirds(scores) {
  const all = allStandings(scores);
  return Object.entries(all)
    .map(([g, s]) => ({ ...s[2], group: g }))
    .sort((a, b) => b.pts - a.pts || b.dg - a.dg || b.gf - a.gf)
    .slice(0, 8);
}

// Returns true only when all 6 matches in a group have scores entered
export function isGroupComplete(gk, scores) {
  const ms = MATCHES[gk] || [];
  return ms.every((_, i) => {
    const sc = scores[gk + "_" + i];
    return sc && sc.h !== "" && sc.a !== "";
  });
}

// Returns confirmed 1st and 2nd of a group, or [null, null] if not complete
export function confirmedTop2(gk, scores) {
  if (!isGroupComplete(gk, scores)) return [null, null];
  const s = calcStandings(gk, scores);
  return [s[0], s[1]];
}

// Returns confirmed best 8 thirds — requires ALL 12 groups complete
export function confirmedBestThirds(scores) {
  const allGroupsComplete = Object.keys(GROUPS).every(g => isGroupComplete(g, scores));
  if (!allGroupsComplete) return [];
  const all = allStandings(scores);
  return Object.keys(GROUPS)
    .map(g => ({ ...all[g][2], group: g }))
    .sort((a, b) => b.pts - a.pts || b.dg - a.dg || b.gf - a.gf)
    .slice(0, 8);
}
